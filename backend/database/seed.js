// Function to generate random discount between 2% and 25%
function generateRandomDiscount() {
  return Math.floor(Math.random() * 24) + 2; // Random number between 2 and 25
}

// Function to calculate prices based on discount
function calculatePrices(basePrice) {
  const discountPercentage = generateRandomDiscount();
  const initialPrice = basePrice;
  const highPrice = Math.round(basePrice * 1.3); // 30% higher than base price
  const offerPrice = Math.round(initialPrice * (1 - (discountPercentage / 100)));
  
  return {
    price: basePrice,
    initialPrice: initialPrice,
    highPrice: highPrice,
    offerPrice: offerPrice,
    discountPercentage: discountPercentage
  };
}

