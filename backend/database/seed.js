async function seedDatabase() {
  try {
    // Connect to MongoDB Atlas
    const mongoURI = process.env.MONGODB_URI || 'mongodb://localhost:27017/ai-ecommerce';
    const mongoOptions = {
      maxPoolSize: 10,
      serverSelectionTimeoutMS: 5000,
      socketTimeoutMS: 45000,
    };
    
    await mongoose.connect(mongoURI, mongoOptions);
    console.log(' Connected to MongoDB Atlas');

    // Clear existing data
    await Product.deleteMany({});
    await User.deleteMany({});
    console.log('Cleared existing data');

    // Hash passwords and create users
    const usersWithHashedPasswords = await Promise.all(
      sampleUsers.map(async (user) => {
        const salt = await bcrypt.genSalt(10);
        const hashedPassword = await bcrypt.hash(user.password, salt);
        return { ...user, password: hashedPassword };
      })
    );

    // Insert users
    const createdUsers = await User.insertMany(usersWithHashedPasswords);
    console.log(`Created ${createdUsers.length} users`);

    // Add seller to each product
    const productsWithSeller = sampleProducts.map(product => ({
      ...product,
      seller: createdUsers[0]._id // Use first user as seller
    }));

    // Insert products
    const createdProducts = await Product.insertMany(productsWithSeller);
    console.log(`Created ${createdProducts.length} products`);

    // Add some sample reviews
    const reviews = [
      {
        user: createdUsers[0]._id,
        name: 'John Doe',
        rating: 5,
        comment: 'Amazing product! Exactly what I was looking for.'
      },
      {
        user: createdUsers[1]._id,
        name: 'Jane Smith',
        rating: 4,
        comment: 'Good quality, but shipping took a while.'
      }
    ];

    // Add reviews to first few products
    for (let i = 0; i < Math.min(5, createdProducts.length); i++) {
      await Product.findByIdAndUpdate(createdProducts[i]._id, {
        $push: { reviews: { $each: reviews } }
      });
    }

    console.log('Added sample reviews');
    console.log('Database seeded successfully!');

    process.exit(0);
  } catch (error) {
    console.error('Error seeding database:', error);
    process.exit(1);
  }
}

seedDatabase();
